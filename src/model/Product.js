class Product {
  constructor(id, name, description, img, price, pricecut, discount, quantity, stars) {
    this.id = id;
    this.name = name;
    this.description = description;
    this.img = img;
    this.price = price;
    this.pricecut = pricecut;
    this.discount = discount;
    this.quantity = quantity;
    this.stars = stars;
  }
}

export default Product;
